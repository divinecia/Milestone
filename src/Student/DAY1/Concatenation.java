public class Concatenation {
    public static void main(String[] args) {
        String id = "123456";
        int age = 22;
        String name = "Divine Iradukunda";
        
        // Concatenate and display the information
        String info = "ID: " + id + ", Age: " + age + ", Name: " + name;
        System.out.println(info);
    }
}

